package ca.flearning.restfulgloom.comp;

public class ItemSummonInfoConverter {
    
    private int hp;
	private int move;
	private int attack;
	private int range;
	
	public ItemSummonInfoConverter() {}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

	public int getMove() {
		return move;
	}

	public void setMove(int move) {
		this.move = move;
	}

	public int getAttack() {
		return attack;
	}

	public void setAttack(int attack) {
		this.attack = attack;
	}

	public int getRange() {
		return range;
	}

	public void setRange(int range) {
		this.range = range;
	}
	
	
}
